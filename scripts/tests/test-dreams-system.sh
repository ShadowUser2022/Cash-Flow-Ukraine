#!/bin/bash

echo "🧪 Тестування системи мрій гравців"
echo "================================="
echo ""

# Перевірка здоров'я сервера
echo "1. 🔍 Перевірка стану сервера..."
health_response=$(curl -s http://localhost:3001/health)
if [[ $? -eq 0 ]]; then
    echo "✅ Сервер працює: $health_response"
else
    echo "❌ Сервер не відповідає"
    exit 1
fi
echo ""

echo "2. 🎮 Інструкції для ручного тестування:"
echo ""
echo "📝 Сценарій тестування мрій:"
echo "   1. Відкрийте два браузери/вкладки на http://localhost:5173"
echo "   2. Створіть нову гру в першому браузері"
echo "   3. Приєднайтеся до гри з другого браузера"
echo "   4. Запустіть гру - у кожного гравця має з'явитися екран вибору мрії"
echo "   5. Оберіть мрію в першому браузері"
echo "   6. Перевірте, що:"
echo "      - Екран вибору мрії зник в першому браузері"
echo "      - Мрія відображається в панелі гравця"
echo "      - Другий гравець бачить повідомлення про вибір мрії"
echo "   7. Оберіть мрію в другому браузері"
echo "   8. Перезавантажте сторінку в першому браузері"
echo "   9. Перевірте, що мрія збереглася після перезавантаження"
echo "   10. Кіньте кубик та перевірте, що екран мрії НЕ з'являється знову"
echo ""

echo "🔍 Що перевіряти:"
echo "   ✅ Екран мрії з'являється тільки один раз на початку"
echo "   ✅ Мрія зберігається на сервері"
echo "   ✅ Мрія синхронізується між гравцями"
echo "   ✅ Мрія відображається в панелі гравця"
echo "   ✅ Після перезавантаження мрія залишається"
echo "   ✅ Екран мрії НЕ з'являється після кожного ходу"
echo ""

echo "🌟 Режим розробника:"
echo "   - В DEV-MODE мрія зберігається тільки локально"
echo "   - Для тестування синхронізації використовуйте звичайний режим гри"
echo ""

echo "💡 Якщо виникають проблеми:"
echo "   - Перевірте консоль браузера на помилки"
echo "   - Перевірте логи backend-сервера"
echo "   - Переконайтеся, що обидва сервери запущені"
echo ""

echo "🚀 Готово до тестування!"
